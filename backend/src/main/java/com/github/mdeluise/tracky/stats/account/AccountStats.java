package com.github.mdeluise.tracky.stats.account;

public class AccountStats {
    private long totalObservations;
    private long totalTrackers;


    public long getTotalObservations() {
        return totalObservations;
    }


    public void setTotalObservations(long totalObservations) {
        this.totalObservations = totalObservations;
    }


    public long getTotalTrackers() {
        return totalTrackers;
    }


    public void setTotalTrackers(long totalTrackers) {
        this.totalTrackers = totalTrackers;
    }
}
